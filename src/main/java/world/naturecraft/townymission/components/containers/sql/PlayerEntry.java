package world.naturecraft.townymission.components.containers.sql;

/**
 * The type Player entry.
 */
public class PlayerEntry {
    private int id;
    private String uuid;
    private String displayName;

    /**
     * Instantiates a new Player entry.
     *
     * @param id          the id
     * @param uuid        the uuid
     * @param displayName the display name
     */
    public PlayerEntry(int id, String uuid, String displayName) {
        this.id = id;
        this.uuid = uuid;
        this.displayName = displayName;
    }

    /**
     * Gets id.
     *
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * Sets id.
     *
     * @param id the id
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * Gets uuid.
     *
     * @return the uuid
     */
    public String getUuid() {
        return uuid;
    }

    /**
     * Sets uuid.
     *
     * @param uuid the uuid
     */
    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    /**
     * Gets display name.
     *
     * @return the display name
     */
    public String getDisplayName() {
        return displayName;
    }

    /**
     * Sets display name.
     *
     * @param displayName the display name
     */
    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }
}
